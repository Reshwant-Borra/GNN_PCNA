def _rcsb_search_all_ids(query_text: str, max_rows: int = 3000) -> list[str]:
    """Return up to max_rows PDB IDs matching query_text via RCSB full-text search."""
    ids = []
    start = 0
    page = 250
    while start < max_rows:
        payload = {
            "query": {
                "type": "terminal",
                "service": "full_text",
                "parameters": {"value": query_text},
            },
            "return_type": "entry",
            "request_options": {
                "paginate": {"start": start, "rows": min(page, max_rows - start)},
                "sort": [{"sort_by": "score", "direction": "desc"}],
            },
        }
        try:
            r = requests.post(
                "https://search.rcsb.org/rcsbsearch/v2/query",
                json=payload, headers=HEADERS, timeout=60,
            )
            r.raise_for_status()
            data = r.json()
            batch = [h["identifier"] for h in data.get("result_set", [])]
            ids.extend(batch)
            if len(batch) < page:
                break
            start += page
        except Exception as e:
            err_log.error("RCSB search '%s' page %d: %s", query_text, start, e)
            break
        time.sleep(0.2)
    return list(dict.fromkeys(ids))  # deduplicate, preserve order


def _rcsb_ligand_bound_ids(max_total: int = 20000) -> list[str]:
    """
    Return IDs of all protein structures that:
    - have at least one small-molecule ligand
    - resolution ≤ 2.5 Å
    Paginate through RCSB search API.
    """
    payload_template = {
        "query": {
            "type": "group",
            "logical_operator": "and",
            "nodes": [
                {
                    "type": "terminal",
                    "service": "text",
                    "parameters": {
                        "attribute": "rcsb_entry_info.resolution_combined",
                        "operator": "less_or_equal",
                        "value": 2.5,
                    },
                },
                {
                    "type": "terminal",
                    "service": "text",
                    "parameters": {
                        "attribute": "rcsb_entry_info.nonpolymer_entity_count",
                        "operator": "greater",
                        "value": 0,
                    },
                },
                {
                    "type": "terminal",
                    "service": "text",
                    "parameters": {
                        "attribute": "rcsb_entry_info.polymer_entity_count_protein",
                        "operator": "greater",
                        "value": 0,
                    },
                },
            ],
        },
        "return_type": "entry",
        "request_options": {
            "paginate": {"start": 0, "rows": 250},
            "sort": [{"sort_by": "rcsb_accession_info.deposit_date", "direction": "desc"}],
        },
    }

    ids = []
    start = 0
    log.info("Paginating RCSB for protein-ligand structures (≤2.5Å)…")
    while start < max_total:
        payload_template["request_options"]["paginate"]["start"] = start
        payload_template["request_options"]["paginate"]["rows"] = min(250, max_total - start)
        try:
            r = requests.post(
                "https://search.rcsb.org/rcsbsearch/v2/query",
                json=payload_template, headers=HEADERS, timeout=60,
            )
            r.raise_for_status()
            data = r.json()
            batch = [h["identifier"] for h in data.get("result_set", [])]
            ids.extend(batch)
            total_count = data.get("total_count", 0)
            log.info("  RCSB ligand-bound: got %d/%d (page start=%d)",
                     len(ids), min(total_count, max_total), start)
            if len(batch) < 250 or len(ids) >= max_total:
                break
            start += 250
        except Exception as e:
            err_log.error("RCSB ligand-bound page %d: %s", start, e)
            break
        time.sleep(0.15)

    return list(dict.fromkeys(ids))


def enumerate_rcsb():
    log.info("=== RCSB PDB enumeration ===")

    # Priority structures
    all_ids = set(RCSB_PRIORITY_IDS)

    # Query-based searches
    for q in RCSB_SEARCH_QUERIES:
        hits = _rcsb_search_all_ids(q, max_rows=2000)
        log.info("  RCSB '%s' → %d hits", q, len(hits))
        all_ids.update(hits)

    # Massive ligand-bound sweep
    ligand_ids = _rcsb_ligand_bound_ids(max_total=20000)
    log.info("  RCSB ligand-bound sweep → %d IDs", len(ligand_ids))
    all_ids.update(ligand_ids)

    log.info("RCSB total unique IDs: %d", len(all_ids))

    # Save ID list
    id_list_file = DIR["rcsb"] / "all_ids.txt"
    id_list_file.write_text("\n".join(sorted(all_ids)), "utf-8")

    # Enqueue downloads
    for pid in all_ids:
        pid = pid.upper()
        # mmCIF.gz (compressed — smaller, faster to transfer)
        DQ.add(
            f"https://files.rcsb.org/download/{pid}.cif.gz",
            DIR["rcsb"] / f"{pid}.cif.gz",
            binary=True, label=f"RCSB {pid} mmCIF.gz",
        )

    return all_ids


# ── AlphaFold — human proteome ────────────────────────────────────────────────
#   Goal: ~18 K reviewed human proteins × ~400 KB CIF = ~7 GB

def _uniprot_human_ids(max_ids: int = 20000) -> list[str]:
    """Fetch reviewed human UniProt accessions via REST API."""
    ids = []
    url = "https://rest.uniprot.org/uniprotkb/search"
    cursor = None
    page_size = 500

    log.info("Fetching human UniProt IDs…")
    while len(ids) < max_ids:
        params = {
            "query":  "reviewed:true AND organism_id:9606",
            "format": "list",
            "size":   page_size,
        }
        if cursor:
            params["cursor"] = cursor

        try:
            r = requests.get(url, headers=HEADERS, params=params, timeout=60)
            r.raise_for_status()
            batch = [x.strip() for x in r.text.strip().splitlines() if x.strip()]
            ids.extend(batch)
            log.info("  UniProt: %d IDs so far", len(ids))

            # Pagination via Link header
            link = r.headers.get("Link", "")
            m = re.search(r'cursor=([^&>]+)', link)
            if m and len(batch) == page_size:
                cursor = m.group(1)
            else:
                break
        except Exception as e:
            err_log.error("UniProt pagination: %s", e)
            break
        time.sleep(0.1)

    return ids[:max_ids]


def enumerate_alphafold():
    log.info("=== AlphaFold DB enumeration ===")
    uniprot_ids = _uniprot_human_ids(max_ids=20000)
    log.info("AlphaFold: %d human UniProt IDs to fetch", len(uniprot_ids))

    # Save ID list
    (DIR["alphafold"] / "uniprot_ids.txt").write_text("\n".join(uniprot_ids), "utf-8")

    # Enqueue CIF downloads directly (EBI provides predictable URLs)
    # Format: https://alphafold.ebi.ac.uk/files/AF-{UNIPROT}-F1-model_v4.cif
    for uid in uniprot_ids:
        for version in ("v4", "v3"):
            cif_url = f"https://alphafold.ebi.ac.uk/files/AF-{uid}-F1-model_{version}.cif"
            DQ.add(
                cif_url,
                DIR["alphafold"] / f"AF-{uid}-F1-model_{version}.cif",
                binary=True, label=f"AlphaFold {uid}",
            )
            break  # try v4 first; fallback handled by retry 404


