# ── progress ───────────────────────────────────────────────────────────────────
_prog_lock = threading.Lock()

def _load_progress() -> dict:
    try:
        if PROGRESS_FILE.exists():
            return json.loads(PROGRESS_FILE.read_text("utf-8"))
    except Exception:
        pass
    return {"done": set(), "errors": [], "bytes": 0}

def _save_progress(p: dict):
    with _prog_lock:
        p2 = {**p, "done": list(p["done"])}
        PROGRESS_FILE.write_text(json.dumps(p2, indent=2), "utf-8")

_P = _load_progress()
_P["done"] = set(_P["done"])

# ── helpers ────────────────────────────────────────────────────────────────────
def _sha(s: str) -> str:
    return hashlib.md5(s.encode()).hexdigest()[:12]

_idx_lock = threading.Lock()
def _write_index(entry: dict):
    with _idx_lock:
        with open(INDEX_FILE, "a", encoding="utf-8") as f:
            f.write(json.dumps(entry) + "\n")

def _bytes_on_disk() -> float:
    total = 0
    for d in DIR.values():
        for p in d.rglob("*"):
            if p.is_file():
                try:
                    total += p.stat().st_size
                except OSError:
                    pass
    return total

