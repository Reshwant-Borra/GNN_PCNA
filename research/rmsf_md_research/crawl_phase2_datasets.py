# ── sync fetch for small metadata calls ────────────────────────────────────────
def _get(url: str, params=None, as_json=False, retries=RETRY_LIMIT):
    for i in range(retries):
        if _stop.is_set():
            return None
        try:
            r = requests.get(url, headers=HEADERS, params=params, timeout=60)
            r.raise_for_status()
            return r.json() if as_json else r.text
        except Exception as e:
            if i == retries - 1:
                err_log.error("GET %s: %s", url, e)
                return None
            time.sleep(2 ** i)

