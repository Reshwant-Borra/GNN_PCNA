def _print_disk_stats():
    gb = _bytes_on_disk() / 1e9
    log.info("Disk: %.2f GB on disk in data/phase2/", gb)
    return gb


def main():
    log.info("=" * 70)
    log.info("Phase-2 Bulk Crawler — target %.0f GB, %d CPU cores",
             TARGET_GB, CPU_WORKERS)
    log.info("Already done: %d URLs, errors: %d, DB bytes: %.2f GB",
             len(_P["done"]), len(_P["errors"]), _P.get("bytes", 0) / 1e9)
    log.info("=" * 70)

    # ── Phase A: enumerate everything into DQ ─────────────────────────────────
    log.info("Phase A: Enumerating download tasks…")

    rcsb_ids = enumerate_rcsb()          # ← also triggers RCSB search pagination
    enumerate_alphafold()
    enumerate_string()
    enumerate_biogrid()
    enumerate_pmc()
    enumerate_biorxiv()
    enumerate_biolip()
    enumerate_scpdb()
    enumerate_asd()
    enumerate_github()
    enumerate_misc()

    log.info("Total tasks queued: %d", len(DQ._pending))

    # ── Phase B: download everything ─────────────────────────────────────────
    log.info("Phase B: Downloading — %d async workers…", ASYNC_CONCURRENCY)
    DQ.run()
    _print_disk_stats()

    # ── Phase C: extract archives on all CPU cores ───────────────────────────
    log.info("Phase C: Extracting archives — %d cores…", CPU_WORKERS)
    archives = list(DATA.rglob("*.gz")) + list(DATA.rglob("*.tar.bz2")) \
             + list(DATA.rglob("*.tgz")) + list(DATA.rglob("*.zip"))
    log.info("Found %d archives to extract", len(archives))
    if archives:
        extract_archives_parallel(archives)
    _print_disk_stats()

    # ── Phase D: parse structures + extract pocket features (CPU bound) ───────
    log.info("Phase D: Structure feature extraction — %d cores…", CPU_WORKERS)

    cif_files = (
        list(DIR["rcsb"].rglob("*.cif")) +
        list(DIR["rcsb"].rglob("*.cif.gz")) +
        list(DIR["alphafold"].rglob("*.cif"))
    )
    log.info("Found %d CIF files to process", len(cif_files))

    # Process in batches to stream results to disk
    for i in range(0, len(cif_files), PROCESS_BATCH):
        if _stop.is_set():
            break
        batch = cif_files[i : i + PROCESS_BATCH]
        log.info("Processing batch %d–%d / %d",
                 i, min(i + PROCESS_BATCH, len(cif_files)), len(cif_files))
        process_structures_parallel(batch)

    _save_progress(_P)
    gb = _print_disk_stats()
    log.info("=" * 70)
    log.info("DONE — %.2f GB on disk, %d structures featurised", gb, len(cif_files))
    log.info("Feature JSONL: %s", FEATURE_FILE)
    log.info("Index JSONL  : %s", INDEX_FILE)
    log.info("=" * 70)


if __name__ == "__main__":
    mp.freeze_support()   # needed on Windows
    main()
