def _process_cif(args) -> dict | None:
    """
    Parse a gzipped or plain mmCIF file with BioPython, extract pocket-relevant
    features, return a dict.  Runs in a worker process — maximises CPU.
    """
    cif_path_str, pdb_id = args
    cif_path = Path(cif_path_str)
    if not cif_path.exists():
        return None

    try:
        from Bio.PDB import MMCIFParser, MMCIF2Dict
        import io, gzip as gz

        raw = cif_path.read_bytes()
        text = gz.decompress(raw).decode("utf-8", errors="replace") \
               if cif_path.suffix == ".gz" else raw.decode("utf-8", errors="replace")

        # --- MMCIF2Dict: fast key-value extraction ---------------------------
        mmcif = MMCIF2Dict.MMCIF2Dict(io.StringIO(text))

        # Resolution
        res = mmcif.get("_refine.ls_d_res_high", ["?"])[0]
        try:
            resolution = float(res)
        except (ValueError, TypeError):
            resolution = None

        # Chains, residues, B-factors
        chain_ids    = mmcif.get("_atom_site.auth_asym_id", [])
        res_names    = mmcif.get("_atom_site.label_comp_id", [])
        b_factors    = mmcif.get("_atom_site.B_iso_or_equiv", [])
        x_coords     = mmcif.get("_atom_site.Cartn_x", [])
        y_coords     = mmcif.get("_atom_site.Cartn_y", [])
        z_coords     = mmcif.get("_atom_site.Cartn_z", [])

        # Numeric arrays
        bvals = []
        for b in b_factors:
            try:
                bvals.append(float(b))
            except (ValueError, TypeError):
                pass

        xs, ys, zs = [], [], []
        for x, y, z in zip(x_coords, y_coords, z_coords):
            try:
                xs.append(float(x)); ys.append(float(y)); zs.append(float(z))
            except (ValueError, TypeError):
                pass

        unique_chains  = list(set(chain_ids))
        unique_residues = list(set(res_names))

        # Ligand residues (non-standard, non-water)
        STANDARD_AA = {
            "ALA","ARG","ASN","ASP","CYS","GLN","GLU","GLY","HIS","ILE",
            "LEU","LYS","MET","PHE","PRO","SER","THR","TRP","TYR","VAL",
            "HOH","WAT","DOD","SO4","PO4","GOL","EDO","MPD","PEG","FMT",
        }
        ligands = [r for r in unique_residues if r not in STANDARD_AA and len(r) <= 3]

        # Pocket score heuristic: high B-factor variance + presence of ligand
        bfactor_variance = float(np.var(bvals)) if len(bvals) > 5 else None
        bfactor_mean     = float(np.mean(bvals)) if len(bvals) > 5 else None

        # Rough bounding box
        bbox = None
        if xs and ys and zs:
            bbox = {
                "x": [round(min(xs), 2), round(max(xs), 2)],
                "y": [round(min(ys), 2), round(max(ys), 2)],
                "z": [round(min(zs), 2), round(max(zs), 2)],
            }

        feature = {
            "pdb_id":          pdb_id,
            "source_file":     cif_path_str,
            "n_atoms":         len(x_coords),
            "n_chains":        len(unique_chains),
            "chains":          unique_chains[:20],
            "n_unique_res":    len(unique_residues),
            "ligands":         ligands[:30],
            "resolution_A":    resolution,
            "bfactor_mean":    bfactor_mean,
            "bfactor_variance": bfactor_variance,
            "bounding_box":    bbox,
            "processed_ts":    datetime.utcnow().isoformat(),
        }
        return feature

    except Exception as e:
        return {"pdb_id": pdb_id, "error": str(e), "source_file": cif_path_str}


def process_structures_parallel(cif_paths: list[Path]):
    """Run _process_cif on all paths using all CPU cores."""
    if not cif_paths:
        return

    log.info("Processing %d structures on %d CPU cores…",
             len(cif_paths), CPU_WORKERS)
    args = [(str(p), p.stem.split(".")[0].upper()) for p in cif_paths]

    with mp.Pool(CPU_WORKERS) as pool:
        results = []
        for i, res in enumerate(pool.imap_unordered(_process_cif, args, chunksize=10)):
            if res:
                results.append(res)
            if (i + 1) % 500 == 0:
                log.info("  processed %d / %d structures", i + 1, len(args))

    # Write feature JSONL
    with open(FEATURE_FILE, "a", encoding="utf-8") as f:
        for r in results:
            f.write(json.dumps(r) + "\n")

    log.info("Feature extraction done — %d entries written to %s",
             len(results), FEATURE_FILE)


def extract_archives_parallel(archive_paths: list[Path]):
    """Extract .gz and .tar.gz files in parallel — CPU intensive."""

    def _extract(p: Path):
        try:
            out_dir = p.parent / p.stem.replace(".tar", "")
            out_dir.mkdir(exist_ok=True)
            if p.name.endswith(".tar.gz") or p.name.endswith(".tar.bz2") or p.name.endswith(".tgz"):
                with tarfile.open(p) as tf:
                    tf.extractall(out_dir)
            elif p.name.endswith(".gz") and not p.name.endswith(".tar.gz"):
                out = out_dir / p.stem
                out.write_bytes(gzip.decompress(p.read_bytes()))
            return str(p)
        except Exception as e:
            err_log.error("Extract %s: %s", p, e)
            return None

    log.info("Extracting %d archives on %d cores…", len(archive_paths), CPU_WORKERS)
    with mp.Pool(CPU_WORKERS) as pool:
        done = list(pool.imap_unordered(_extract, archive_paths, chunksize=4))
    log.info("Extracted %d archives", sum(1 for d in done if d))


