class DownloadQueue:
    """Thread-safe queue of (url, out_path, binary, label) tasks."""

    def __init__(self):
        self._q: asyncio.Queue = None   # created in async context
        self._pending: list[tuple] = []
        self._lock = threading.Lock()

    def add(self, url: str, out_path: Path, binary: bool = True, label: str = ""):
        uid = _sha(url)
        if uid in _P["done"] or out_path.exists():
            return
        with self._lock:
            self._pending.append((url, out_path, binary, label, uid))

    def add_batch(self, items):
        for item in items:
            self.add(*item)

    async def _run(self):
        self._q = asyncio.Queue()
        for item in self._pending:
            await self._q.put(item)

        sem = asyncio.Semaphore(ASYNC_CONCURRENCY)

        async def worker(session):
            while True:
                try:
                    url, out_path, binary, label, uid = self._q.get_nowait()
                except asyncio.QueueEmpty:
                    return

                if uid in _P["done"] or out_path.exists():
                    self._q.task_done()
                    continue

                async with sem:
                    for attempt in range(RETRY_LIMIT):
                        if _stop.is_set():
                            self._q.task_done()
                            return
                        try:
                            async with session.get(url) as resp:
                                if resp.status == 404:
                                    err_log.error("404 %s", url)
                                    break
                                resp.raise_for_status()
                                content = await resp.read()
                                out_path.parent.mkdir(parents=True, exist_ok=True)
                                out_path.write_bytes(content)
                                with _prog_lock:
                                    _P["done"].add(uid)
                                    _P["bytes"] = _P.get("bytes", 0) + len(content)
                                if len(self._pending) % 200 == 0:
                                    _save_progress(_P)
                                _write_index({
                                    "url": url, "label": label,
                                    "file": str(out_path),
                                    "bytes": len(content),
                                    "ts": datetime.utcnow().isoformat(),
                                })
                                log.debug("[DL] %s  %.1f KB",
                                          label or url[:70], len(content)/1024)
                                break
                        except Exception as e:
                            if attempt == RETRY_LIMIT - 1:
                                err_log.error("FAIL %s: %s", url, e)
                                with _prog_lock:
                                    _P["errors"].append(url)
                            else:
                                await asyncio.sleep(2 ** attempt)
                self._q.task_done()

        connector = aiohttp.TCPConnector(
            limit=ASYNC_CONCURRENCY,
            ttl_dns_cache=300,
            ssl=False,
        )
        async with aiohttp.ClientSession(
            headers=HEADERS,
            timeout=REQUEST_TIMEOUT,
            connector=connector,
        ) as session:
            workers = [
                asyncio.create_task(worker(session))
                for _ in range(ASYNC_CONCURRENCY)
            ]
            await asyncio.gather(*workers)

        _save_progress(_P)
        log.info("Download batch done — %d total fetched, %.2f GB on disk",
                 len(_P["done"]), _bytes_on_disk() / 1e9)

    def run(self):
        log.info("Launching %d async download workers for %d tasks…",
                 ASYNC_CONCURRENCY, len(self._pending))
        asyncio.run(self._run())


